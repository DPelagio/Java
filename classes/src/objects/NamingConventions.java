package objects;
/**
 * Upper camel case for Classes & Interfaces
 * Classes names are typically be nouns i.e. "Car"
 * Interfaces names are typically adjectives i.e. "Runnable"
 */
public class NamingConventions {
    /* 
     * Capital letter for constants and "_" used as separator
     */ 
    static final int A_CONSTANT_IN_JAVA = 5;

    /**
     * Lower camel case for methods
     * Lower camel case for variables
     */
    int varOne = 1;
    public void doSomethingGreat(int varTwo){
        int varThree = 5; 
        System.out.println("As goos as it gets !! :) ");
    }
}
